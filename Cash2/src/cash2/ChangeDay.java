    public ChangeDay(String date, MFrame mf) throws ParseException {
        initComponents();
        setTitle(date);
        setLocationRelativeTo(null);//center form
        this.mf = mf;
        this.date = date;
        jTextField1.setText(d.getRest(date)+"");
        jTextField3.setText(d.getNal(date)+"");
        merchants = m.getMerchantsHours(date);
        String merch_h = "";
        Iterator it = merchants.entrySet().iterator();
        while (it.hasNext()) {
            Map.Entry pairs = (Map.Entry) it.next();
            merch_h += "\n" + (String) pairs.getKey() + " " + (int) pairs.getValue() + "ч";
        }
        jTextArea1.setText(merch_h);
        ArrayList<String> list = m.getListOfWorkingMerchants();
        jComboBox1.setModel(new DefaultComboBoxModel(list.toArray()));
        pack();
        setVisible(true);
    }

