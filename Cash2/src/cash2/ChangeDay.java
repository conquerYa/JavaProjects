    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:

        name = jComboBox1.getSelectedItem().toString();
        if (merchants.containsKey(name)) {
            JOptionPane.showMessageDialog(null, "Продавец уже добавлен");
        } else {
            String h = jTextField4.getText();
            if (h.matches(digits)) {
                Integer hours = Integer.parseInt(h);
                if (hours < 0) {
                    JOptionPane.showMessageDialog(null, "Время работы не может быть отрицательным");
                } else if (hours > 24) {
                    JOptionPane.showMessageDialog(null, "Время работы не может быть более 24ч ");
                } else {
                    merchants.put(name, hours);
                    jTextArea1.setText(jTextArea1.getText() + "\n" + name + hours+"ч");
                }
            } else {
                JOptionPane.showMessageDialog(null, "Проверьте правильность ввода времени работы");
            }
        }
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        int iLine = jTextArea1.getText().lastIndexOf('\n');
        if (iLine >= 0) {
            jTextArea1.setText(jTextArea1.getText().substring(0, iLine));
            List<String> list = new ArrayList<>(merchants.keySet());
            merchants.remove(list.get(list.size()-1));
        }
    }//GEN-LAST:event_jButton3ActionPerformed

    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:
        String ost = jTextField1.getText();
        String nal = jTextField3.getText();
        String merchT = jTextArea1.getText();
        if (date.equals("") || ost.equals("") || nal.equals("") || merchT.equals("")) {
            JOptionPane.showMessageDialog(null, "Заполните, пожалуйста, все поля");
        } else if (!ParseDate.isValid(date)) {
            JOptionPane.showMessageDialog(null, "Дата введена не корректно");
        } else if (!ost.matches(digits)) {
            JOptionPane.showMessageDialog(null, "В поле Остаток могут быть лишь цифры");
        } else if (!nal.matches(digits)) {
            JOptionPane.showMessageDialog(null, "В поле Наличные могут быть лишь цифры");
        } else {
            d = new Day();
            int t = d.updateDay(date, Integer.parseInt(ost), Integer.parseInt(nal), merchants);
            if (t == 0) {
                JOptionPane.showMessageDialog(null, "Error, sorry");
            } else {                
                mf.refresh();
                JOptionPane.showMessageDialog(null, "День успешно изменён");
                dispose();
            }
        }
    }//GEN-LAST:event_jButton4ActionPerformed

